function updateTableContent() {
  const documentData = JSON.parse(localStorage.getItem("documentData")) || {};

  const practice = practiceData.find(
    (plan) => plan.name === documentData["Вид практики"]
  );
  const practiceType = practice.types.find(
    (type) => type.type === documentData["Тип практики"]
  );

  const planTableBody = document.querySelector(".table.plan tbody");

  if (planTableBody && practice) {
    planTableBody.innerHTML = "";

    practice.plan.forEach((stage) => {
      const row = document.createElement("tr");
      row.innerHTML = `
                <td></td> <!-- Пустая ячейка для даты -->
                <td>${stage.stage}</td>
                <td>${stage.tasks}</td>
                <td></td> <!-- Пустая ячейка для отметки о выполнении -->
            `;
      planTableBody.appendChild(row);
    });
  }

  const competenciesTableBody = document.querySelector(
    ".table:nth-of-type(3) tbody"
  );

  if (competenciesTableBody && practiceType) {
    competenciesTableBody.innerHTML = "";

    practiceType.competencies.forEach((competency) => {
      const row = document.createElement("tr");
      row.innerHTML = `
                <td></td> <!-- Пустая ячейка для даты -->
                <td>${competency}</td>
                <td></td> <!-- Пустая ячейка для подписи студента -->
                <td></td> <!-- Пустая ячейка для подписи руководителя -->
            `;
      competenciesTableBody.appendChild(row);
    });
  }
}

